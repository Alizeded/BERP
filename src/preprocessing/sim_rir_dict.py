class RoomCases:
    def calculate_distance(self, src, mic):
        """Calculate Euclidean distance between source and microphone"""
        return np.round(np.sqrt(np.sum((np.array(src) - np.array(mic)) ** 2)), 3)

    def but_room_cases_gen(self, sources, positions, volume):
        cases = {}
        case_num = 1
        for src in sources:
            for mic in positions:
                cases[f"case_{case_num}"] = {
                    "src": src,
                    "mic": mic,
                    "volume": volume,
                    "distRcv": self.calculate_distance(src, mic),
                }
                case_num += 1

        return cases

